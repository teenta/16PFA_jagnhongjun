# -*- coding: utf8 -*-
import math

print ("1234567890" * 4)
print ("%d" % (math.pi))
print ("%5d" % (math.pi))
print ("%5f" % (math.pi))
print ("%8f" % (math.pi))
print ("%+8f" % (math.pi))

print ("%16.8f" % (math.pi))
print ("1234567890" * 4)

